
public class OperadoresMatematicos {
	public static void main(String[] args) {
		System.out.println("5/2=" + (5 / 2)); // DIVISÃO
		System.out.println("5%2=" + (5 % 2)); // RESTO DE DIVISÃO
		System.out.println("6/2=" + (6 / 2)); // DIVISÃO
		System.out.println("6%2=" + (6 % 2)); // RESTO DE DIVISÃO

		int x = 5;
		System.out.println("x=" + x);
		x++; // x=x+1
		System.out.println("x=" + x);
		x--; // x=x-1
		System.out.println("x=" + x);
		x += 10; // x=x+10;
		System.out.println("x=" + x);
		x *= 5; // x=x*5;
		System.out.println("x=" + x);
		System.out.println("5 + 5 / 2=" + (5 + 5 / 2));
		System.out.println("(5 + 5) / 2=" + ((5 + 5) / 2)); 
	}

}
